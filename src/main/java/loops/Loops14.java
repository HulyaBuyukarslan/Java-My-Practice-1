package loops;

public class Loops14 {
    public static void main(String[] args) {

//  3 ile 9 arasındaki tam sayıların çarpımını bulmak için kodu yazınız.

        int carpim = 1;
        int i = 3;

        while (i < 4) {


           carpim = carpim * i;
            i++;


        }
        System.out.println(carpim);


    }

}
